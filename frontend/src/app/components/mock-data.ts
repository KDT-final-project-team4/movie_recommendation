export interface Movie {
  id: number;
  title: string;
  poster: string;
  score: number;
}

const MOVIE_TITLES = [
  "Neon Requiem", "Starfall", "The Last Signal", "Love in Transit", "Midnight Protocol",
  "Ember & Ash", "Parallel Drift", "The Void Keeper", "Crimson Tide", "Echo Chamber",
  "Quantum Leap", "Infinite Loop", "Solar Winds", "Dark Horizon", "The Deep Below",
];

export function generateMovies(seed: number, count: number, scoreType: "percent" | "decimal"): Movie[] {
  return Array.from({ length: count }, (_, i) => {
    const idx = (seed + i) % POSTERS.length;
    const rawScore = 70 + Math.floor(((seed * 7 + i * 13) % 30));
    const score = scoreType === "percent" ? rawScore : parseFloat((rawScore / 100).toFixed(2));
    return {
      id: seed * 100 + i,
      title: MOVIE_TITLES[(seed + i) % MOVIE_TITLES.length],
      poster: POSTERS[idx],
      score,
    };
  });
}
